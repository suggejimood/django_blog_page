def getDate(post):
    return post["date"]

def index(request):
    sortedPost = sorted(allPosts, key=getDate)
    latestPosts = sortedPost[-3:]
    return render(request, "blog/index.html", {"posts": latestPosts})

def posts(request):
    return render(request, "blog/posts.html", {"posts": allPosts})

def post(request, slug):
    post = next((post for post in allPosts if post['slug'] == slug), None)

    if post:
        return render(request, "blog/post.html", {"post": post})
    else:
        raise Http404()